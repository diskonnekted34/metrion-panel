export const getModeLabel = (mode: PositionMode): string => {
  switch (mode) {
    case "advisory_dominant": return "AI Danışman Dominant";
    case "assisted": return "Asiste Mod";
    case "autopilot": return "Tam Otopilot";
  }
};

export const getModeColor = (mode: PositionMode): string => {
  switch (mode) {
    case "advisory_dominant": return "text-violet-400 bg-violet-400/10 border-violet-400/20";
    case "assisted": return "text-primary bg-primary/10 border-primary/20";
    case "autopilot": return "text-purple-600 bg-purple-600/10 border-purple-600/20";
  }
};

export const getCategoryLabel = (cat: SeatCategory): string => {
  switch (cat) {
    case "executive": return "Üst Yönetim";
    case "department": return "Departman";
    case "creative_intelligence": return "Kreatif & İstihbarat";
  }
};

